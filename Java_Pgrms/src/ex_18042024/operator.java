public class operator {

	public static void main(String[] args) {
		//Arithmetic Operators
		int a = 20;
		int b = 30;
		System.out.println(b+a);
		System.out.println(b-a);
		System.out.println(b*a);
		System.out.println(b/a);
		System.out.println(b%a); // Modulus -> Remainder


	}

}




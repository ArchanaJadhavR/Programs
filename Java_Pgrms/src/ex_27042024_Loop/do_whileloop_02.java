package ex_27042024_Loop;

public class do_whileloop_02 {

	public static void main(String[] args) {
		

        int i = 10;
        do {
            System.out.println(i);
            i++;
        } while (i < 10);


        while(i < 10){
            System.out.println(i);
            i++;
        }
    }
}

package ex_27042024;

public class whileloop_basic {

	public static void main(String[] args) {
	
		//while
        // A - Init
        // B - Condition
        // C - Incre

        int i = 1; // A
        while (i<=10){ // B
            // Execute the code
            System.out.println(i);
            i++; // C

            // i - 1, 10 , print 1 to 10
        }

        for (int j = 1; j <=10 ; j++) //        or -> for (int j = 1; j <=10 ; j=j+1)
        
        {
            System.out.println(j); // using for loop
        }

    }

	}


